      origin: "top",
    });

    sr.reveal(".logos", {
      interval: 100,
      distance: "100px",
      origin: "bottom",
      opacity: 0,
    });

    sr.reveal(".animasyonlar", {
      interval: 100,
      distance: "100px",
      origin: "bottom",
      opacity: 0,
    });

    sr.reveal(".reveal", {
      duration: 1500,
      distance: "100px",
      origin: "bottom",
      opacity: 0,
    });

    sr.reveal(".animasyon", {
      duration: 500,
      interval: 1000,
      distance: "100px",
      origin: "top",
      opacity: 0,
    });

    sr.reveal(".animasyon-1", {
      duration: 500,
      interval: 500,
      distance: "100px",
      origin: "top",
      opacity: 0,
    });

    sr.reveal(".animat", {
      duration: 500,
      interval: 300,
      distance: "10px",
      origin: "bottom",
    });
  },

  methods: {
    mail_gonder() {
      if (
        this.mailinfo.isim != "" &&
        this.mailinfo.eposta != "" &&
        this.mailinfo.icerik != ""
      ) {
        this.$store.commit("loading", "block");
        mailgonder
          .addemail(this.mailinfo)
          .then((response) => {
            this.eposta();
            this.$store.commit("loading", "none");
            this.bildirimler(
              "Mail Başarıyla Gönderildi !",
              "Sayın" +
                " " +
                this.mailinfo.isim +
                " " +
                "E-Mailinizi Aldım. En Kısa Sürede Size Cevap Vereceğim !",
              "b-toaster-top-center",
              "success"
            );
          })
          .catch((err) => {
            this.$store.commit("loading", "none");
            this.bildirimler(
              "Mail Gönderilemedi !",
              "E-Mailiniz Gönderilirken Hata Oluştu !",
              "b-toaster-top-center",
              "danger"
            );
          });
      } else {
        this.bildirimler(
          "Mail Gönderilemedi !",
          "Boş Alan Bırakmayınız !",
          "b-toaster-top-center",
          "danger"
        );
      }
    },
    eposta() {
      mailgonder
        .getemail()
        .then((response) => {})
        .catch((err) => {});
    },
    bildirimler(bildirim, aciklama, konum, renk) {
      this.$bvToast.toast(aciklama, {
        title: bildirim,
        toaster: konum,
        autoHideDelay: 3000,
        variant: renk,
      });
    },
  },
  created() {},
};
</script>

