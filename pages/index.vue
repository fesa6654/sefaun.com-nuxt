import mailgonder from "../servisler/email";

export default {
  data() {
    return {
      year: new Date().getFullYear(),
      mailinfo: {
        isim: "",
        eposta: "",
        icerik: "",
      },
    };
  },
  computed: {},
  mounted() {
    window.sr = new ScrollReveal();
    /*
